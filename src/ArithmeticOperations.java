
public class ArithmeticOperations {

	public static void main(String[] args) {
		int a=15, b=4;
		
		int sum=a+b;
		int diff=a-b;
		int mul=a*b;
		int div=a/b;
		int rem=a%b;
		System.out.println(sum);
		System.out.println(diff);
		System.out.println(mul);
		System.out.println(div);
		System.out.println(rem);

	}

}
